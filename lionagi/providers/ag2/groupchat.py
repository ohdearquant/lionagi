# --- Models (structured output targets) ---


class HandoffCondition(BaseModel):
    """Condition for handing off to another agent in AG2 GroupChat."""

    target: str = Field(description="Target agent name to hand off to")
    condition: str = Field(
        description="Natural language condition for handoff trigger",
    )


class AgentSpec(BaseModel):
    """Specification for a single AG2 ConversableAgent in a GroupChat."""

    name: str = Field(description="Agent name (e.g. 'Researcher', 'Analyst')")
    role: str = Field(description="One-line role description")
    system_message: str = Field(default="", description="Full system prompt for the agent")
    tools: list[str] = Field(
        default_factory=list,
        description="Tool names from the registry this agent can invoke",
    )
    handoffs: list[HandoffCondition] = Field(
        default_factory=list,
        description="Conditions for handing off to other agents",
    )
    nlip_url: str | None = Field(
        default=None,
        description="If set, this agent is a remote NLIP agent at the given URL. "
        "system_message and llm_config are ignored — the remote server handles them.",
    )
    state_template: str | None = Field(
        default=None,
        description=(
            "Dynamic system message template using context variables. "
            "Maps to AG2 v0.12 UpdateSystemMessage. "
            "Example: 'Insights stored: {insights_stored}'"
        ),
    )


class GroupChatSpec(BaseModel):
    """Specification for a single AG2 GroupChat — one node in a DAG."""

    name: str = Field(description="GroupChat identifier")
    objective: str = Field(description="What this GroupChat should accomplish")
    agents: list[AgentSpec] = Field(description="Participating agents")
    max_round: int = Field(default=15, gt=0, description="Maximum conversation rounds")
    context: dict[str, Any] = Field(
        default_factory=dict,
        description="Initial context variables shared across agents",
    )


class ResearchPlan(BaseModel):
    """Full research plan generated by LLM via structured output."""

    topic: str = Field(description="The research topic")
    hypothesis: str = Field(description="Central hypothesis to test")
    group_chats: list[GroupChatSpec] = Field(
        description="Independent GroupChat teams running in parallel",
    )
    synthesis_instruction: str = Field(
        description="How to combine all GroupChat outputs",
    )
    expected_output: str = Field(
        description="What the final deliverable should look like",
    )
    safeguard_policy: list[str] = Field(
        default_factory=list,
        description="Research governance guidelines enforced at conversation level",
    )


class AG2GroupChatRequest(BaseModel):
    """Request for AG2 GroupChat endpoint."""

    messages: list[dict[str, Any]] = Field(default_factory=list)
    prompt: str = ""
    max_round: int = Field(default=15, gt=0)
    context_variables: dict[str, Any] = Field(default_factory=dict)


